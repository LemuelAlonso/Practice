public class MathTest{
    public static void main(String[] args) {
        double value = 40;
        System.out.println("SQRT: " + Math.sqrt(value));
        System.out.println("SIN: " + Math.sin(value));
        System.out.println("COS: " + Math.cos(value));
        System.out.println("FLOOR: " + Math.floor(value));
        System.out.println("CEIL: " + Math.ceil(value));
        System.out.println("ROUND: " + Math.round(value));
        System.out.println("MAX: " + Math.max(80, 50));
        System.out.println("MIN: " + Math.min(80, 50));
        System.out.println("RANDOM: " + Math.random() * 100);
    }
}